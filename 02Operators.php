<?php
/*Arithmetic Operator
+ = Addition
- = Subtraction
* = Multiplication
/ = Division
% = Modulo
** = Exponentiation */

$num1 = 10;
$num2 = 5;

$Addition = $num1 + $num2;
echo "{$num1} + {$num2} = {$Addition}\n";

$Subtraction = $num1 - $num2;
echo "{$num1} - {$num2} = {$Subtraction}\n";

$Multiplication = $num1 * $num2;
echo "{$num1} * {$num2} = {$Multiplication}\n";

$Division = $num1 / $num2;
echo "{$num1} / {$num2} = {$Division}\n";